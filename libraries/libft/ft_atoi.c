#include "libft.h"

int	ft_atoi(const char *str)
{
	int	res;
	int	sign;

	res = 0;
	sign = 1;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		if (res > INT_MAX / 10 || res * 10 > INT_MAX - (*str - '0'))
			return (-1);
		res = res * 10 + (*str - '0');
		str++;
	}
	return (sign * res);
}
