#include "libft.h"

long	ft_atol(const char *str)
{
	long long	res;
	long long	sign;

	res = 0;
	sign = 1;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		if (res > LONG_MAX / 10 || res * 10 > LONG_MAX - (*str - '0'))
			return (-1);
		res = res * 10 + (*str - '0');
		str++;
	}
	return (sign * res);
}
